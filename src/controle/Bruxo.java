package controle;

public class Bruxo {
	private Integer id;      
    private String nome;   
    private Boolean sangue; 
    private String casa;   
    private String animal_nome; 
    private String animal_tipo;
	
    
    
    public Integer getId() {
		return id;
	}
	
    public void setId(Integer id) {
		this.id = id;
	}
	
    public String getNome() {
		return nome;
	}
	
    public void setNome(String nome) {
		this.nome = nome;
	}
	
    public Boolean getSangue() {
		return sangue;
	}
	
    public void setSangue(Boolean sangue) {
		this.sangue = sangue;
	}
	
    public String getCasa() {
		return casa;
	}
	
    public void setCasa(String casa) {
		this.casa = casa;
	}
	
    public String getAnimal_nome() {
		return animal_nome;
	}
	
    public void setAnimal_nome(String animal_nome) {
		this.animal_nome = animal_nome;
	}
	
    public String getAnimal_tipo() {
		return animal_tipo;
	}
	
    public void setAnimal_tipo(String animal_tipo) {
		this.animal_tipo = animal_tipo;
	}
    
    
}
